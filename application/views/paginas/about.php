<p><b>Acerca de</b></p>

<p>
<em>Síntoma de última década </em> es un archivo de Punk Colombiano que reune registros sonoros y audiovisuales desde los inicios del movimiento hasta el 2007. El nombre fue tomado de la famosa <a href="https://www.youtube.com/watch?v=GjJT5wW_9hI">canción Dexkoncierto</a>.
</p>

<p>
Hace 6 años <em>Síntoma de última década </em> empezó <a href="https://sintomadeultimadecada.wordpress.com">como un Blog</a>  con la idea de subir discos que no habían sido re-editados y que no estaban digitalizados, acompañados con algún pequeño escrito acerca del disco o como se obtuvo.<br>
Con el tiempo hacer estas cosas consumían mucho tiempo y no era tan fácil encontrar con estos discos por lo cual fue abandonado. En esta página retomamos una idea similar (esto es, subir discos) pero ahora en una forma de archivo abierto para el que quiera consultarlo sin dedicarle a cada disco en específico tiempo extra.
</p>

<p>
Este es un proyecto autogestionado. El material acá presente se ha conseguido entre prestamos de amigos o amigos de amigos y partes de las (pequeñas) colecciones personales. Por tanto si usted, persona lectora de estas líneas, tiene algo que no se encuentre acá que quiera ora prestarnos (¡prometemos cuidarlo muy bien!) ora donarnos, <a href="contact.php">acá nos puede contactar</a>. 
</p>


<p>
